    /**
     * @param args the command line arguments
     */
    public void cargarTabla(){
        DefaultTableModel tablaModel= new DefaultTableModel(){
           
         @Override
         public boolean isCellEditable (int row, int column){
             return false;
         }
           
       };
        String titulos []={"Fecha","Hora","Nombre","Due√±o","Cel."};
        tablaModel.setColumnIdentifiers(titulos);
        Date busqueda = calendario.getDate();
        List<Turno> listaDeTurnos = control.traerTurnos();
        String _fecha;
        if(listaDeTurnos!=null){
            
            for(Turno turno:listaDeTurnos){
                
                if(turno.getDia().equals(busqueda)){ 
                    _fecha = sdf.format(turno.getDia());
                    
                    Object[] objeto = {_fecha,turno.getHora(),turno.getMascota().getNombre().toUpperCase(),turno.getMascota().getDuenio().getNombre().toUpperCase(),turno.getMascota().getDuenio().getCelDuenio()};
                    tablaModel.addRow(objeto);
                } 
            }
        }
        tablaVerTurnoPorfecha.setModel(tablaModel);
        
    
        
        
        
        
    }
    

